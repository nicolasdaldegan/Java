
public class Ex1 {

	public static void main(String[] args) {
		System.out.println("Números de 1 até 100");
		for(int i = 1; i<=100;) {
			System.out.println(i);
			i++;
		}
		System.out.println("Números de 100 até 1");
		for(int i = 100; i>=1;) {
			System.out.println(i);
			i--;
		}

	}

}
